     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $topNews = News::query()
            ->select('id', 'name', 'view_count','author')
            ->orderBy('view_count', 'desc')
            ->take(5)
            ->get();

        $topAuthors = News::query()
            ->selectRaw('author, SUM(view_count) as view_count')
            ->orderBy('view_count', 'desc')
            ->groupBy('author')
            ->take(5)
            ->get();

        return view('pages.admin.dashboard.index', compact('topNews', 'topAuthors'));
    }

